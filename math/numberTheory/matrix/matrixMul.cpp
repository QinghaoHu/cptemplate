// this is the template for mutiple the matrix
int n;

void mulself (int *a) {
    int result[n][n];
    for (int i = 1; i <= n; i++) {
        for (int j = 1; j <= n; j++) {
            for (int k = 1; k <= n; k++) {
                result[i][j] += a[i][k] * a[k][j];
            }
        }
    }
    memcpy(a, result, sizeof a);
}